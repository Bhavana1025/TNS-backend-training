//program to print all elements of integer array
public class Foreachloop {

	public static void main(String[] args) {
	  int[]a = {1,2,3,45,6};
	  for(int i:a) {
		  System.out.println(i);
	  }
           //print all characters in a character array
		char ch[] = {'A','D','B','M'};
		for(char c:ch) {
			System.out.println(c);
		}
	}

}
