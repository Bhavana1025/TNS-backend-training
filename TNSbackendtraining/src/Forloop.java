//print even numbers
public class Forloop {

	public static void main(String[] args) {
		System.out.println("the even numbers between 1 to 50: ");
         for(int i=1; i<=50; i++) {
        	 //to check number is even or odd
        	 if(i%2==0) {
        		 System.out.println(i+" ");
        	 }
         }
	}

}
