
public class Operators {

	public static void main(String[] args) {
		//unary operators
		int a = 5;
		a++;
		System.out.println(a);
		int b = 6;
			++b;
			System.out.println(b);
          int c = 7;
          //here first it have to print the number and then increment
          int d = c++;
          System.out.println(d);
          int e = 8;
          //here it have to print the incremented number 
          int f = ++e;
          System.out.println(f);
	}

}
