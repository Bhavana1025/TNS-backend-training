//Encapsulation
package com.tns.oopsDemo;

public class Student {
	private int sage;
	private String sname;
	public void setsage(int sage) {
		this.sage =sage;
	}
	public int getsage() {
		return sage;
	}
	public void  setsname(String sname) {
		this.sname=sname;
	}
	public String getsname() {
	return sname;
	}

}
